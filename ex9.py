print()

numero = 10
print("Valor inicial:", numero)

numero += 5
print("Após adicionar 5 (+=):", numero)

numero *= 2
print("Após multiplicar por 2 (*=):", numero)

numero -= 8
print("Após subtrair 8 (-=):", numero)

numero /= 3
print("Após dividir por 3 (/=):", numero)

numero %= 4
print("Após calcular o resto da divisão por 4 (%=):", numero)

numero **= 2
print("Após elevar ao quadrado (**=):", numero)
